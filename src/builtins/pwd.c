#include <unistd.h>

int builtin_pwd(void)
{
    char    *path;

    path = getcwd(NULL, 0);
    if (!path)
    {
        write (2, "error\n", 6);
        return (1);
    }
    ft_printf("%s\n", path);
    free(path);
    return (0);
}
